print('---- tuple Index Method -----')

my_tuple = (1, 2, 3, 'a', 'b', 'a')
index_of_2 = my_tuple.index(2)
index_of_a = my_tuple.index('a')

print(index_of_2)  # Output: 1
print(index_of_a)  # Output: 3

print()


