print('---- Tuple Count Method ------')

my_tuple = (1, 2, 3, 2, 'a', 'b', 'a')
count_of_2 = my_tuple.count(2)
count_of_a = my_tuple.count('a')

print(count_of_2)  # Output: 2
print(count_of_a)  # Output: 2

print()

